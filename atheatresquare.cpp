#include<bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n,m,a;
    cin>>n>>m>>a;
    int r=n/a;
    int p=m/a;
    int k,l,s;


    for(int i=0; i=r; i++){
        k=k+1;
        for (int j = 0; j = p; j++)
        {
            l=l+1;
        }
        
        
    }
    s=k*l;
    cout<<s;
    return 0;
}